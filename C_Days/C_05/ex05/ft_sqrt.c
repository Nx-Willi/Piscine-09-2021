int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 1 || nb == 2)
		return (0);
	while (i * i < nb && i < 46341)
		i++;
	if (nb % i != 0)
		return (0);
	return (i);
}
