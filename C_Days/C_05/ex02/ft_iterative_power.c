int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	nb_fix;

	i = 1;
	nb_fix = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (i < power)
	{
		nb = nb * nb_fix;
		i++;
	}
	return (nb);
}
